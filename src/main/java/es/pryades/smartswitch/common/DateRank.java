package es.pryades.smartswitch.common;

import java.io.Serializable;
import java.util.Date;

/**
 * 
 * @author Dismer Ronda
 *
 */
public class DateRank implements Serializable
{
	
	//###########################################################################################//
	//#										PROPERTIES										   	#//
	//###########################################################################################//
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -6635056571699384512L;
	private Date iniDate;
	private Date endDate;
	
	//###########################################################################################//
	//#										CONSTRUCTOR(S)									   	#//
	//###########################################################################################//
	
	public DateRank(){
		
	}
	
	public DateRank(Date iniDate, Date endDate) 
	{
		this.iniDate = iniDate;
		this.endDate = endDate;
	}
	
	//###########################################################################################//
	//#											ACCESS METHOD(S)							   	#//
	//###########################################################################################//	
	
	/**
	 * 
	 * @return
	 */
	public Date getIniDate(){
		return iniDate;
	}
	
//------------------------------------------------------------------------------------------------------------
	/**
	 * 
	 * @return
	 */
	public Date getEndDate(){
		return endDate;
	}
	
//------------------------------------------------------------------------------------------------------------
	/**
	 * 
	 * @param ini
	 * @param end
	 */
	public void setRank(Date ini, Date end){
		this.iniDate = ini;
		this.endDate = end;
	}

//------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------------------
}
